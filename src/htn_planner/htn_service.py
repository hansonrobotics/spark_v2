# PLAN EXECUTOR
# ═══════════════════════════════════════════════════════════════════════════════

class PlanExecutor:
    def __init__(self, planner: DynamicHTNPlanner, robot_interface=None):
        self.planner = planner
        self.robot = robot_interface

    async def execute_plan(self, plan: List[PrimitiveTask],
                            trace: PlanTrace = None) -> Dict[str, Any]:
        results = []
        total_cost = 0
        start_time = time.time()
        for task in plan:
            t0 = time.time()
            try:
                if self.robot:
                    result = await self.robot.execute(task.name, task.params)
                else:
                    result = {"status": "simulated", "task": task.name}
                task.status = TaskStatus.COMPLETED
                task.result = result
            except Exception as e:
                task.status = TaskStatus.FAILED
                task.result = {"error": str(e)}
            elapsed = time.time() - t0
            total_cost += task.cost
            results.append({
                "task_id": task.task_id, "task_name": task.name,
                "status": task.status.value, "elapsed_ms": elapsed * 1000,
            })
            if task.status == TaskStatus.FAILED:
                if trace:
                    trace.success = False
                    trace.total_time = time.time() - start_time
                    trace.total_cost = total_cost
                    self.planner.record_execution_outcome(trace)
                return {"status": "partial_failure", "results": results}
        if trace:
            trace.success = True
            trace.total_time = time.time() - start_time
            trace.total_cost = total_cost
            self.planner.record_execution_outcome(trace)
        return {"status": "success", "results": results,
                "total_cost": total_cost}


