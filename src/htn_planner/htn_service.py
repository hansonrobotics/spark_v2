    def _define_compound(self, name: str, description: str, tags: List[str]):
        self.tasks[name] = TaskDefinition(
            name=name, description=description, is_primitive=False,
            mutability=TaskMutability.STABLE, tags=tags, created_by="system",
        )

    def add_task(self, task_def: TaskDefinition) -> TaskDefinition:
        if task_def.name in self.tasks:
            existing = self.tasks[task_def.name]
            if existing.mutability == TaskMutability.ANCHORED:
                raise ValueError(f"Cannot modify anchored task: {task_def.name}")
        self.tasks[task_def.name] = task_def
        logger.info(f"Registered task: {task_def.name} "
                    f"(primitive={task_def.is_primitive}, "
                    f"mutability={task_def.mutability.value})")
        return task_def

    def add_method(self, method: Method) -> Method:
        if method.task_name not in self.methods:
            self.methods[method.task_name] = []
        sig = self._method_signature(method)
        for existing in self.methods[method.task_name]:
            if self._method_signature(existing) == sig and not existing.deprecated:
                return existing
        self.methods[method.task_name].append(method)
        self._sort_methods(method.task_name)
        logger.info(f"Registered method: {method.name} for {method.task_name} "
                    f"(origin={method.origin.value}, confidence={method.confidence:.2f})")
        return method

    def deprecate_method(self, method_id: str, reason: str = ""):
        for methods_list in self.methods.values():
            for m in methods_list:
                if m.method_id == method_id:
                    m.deprecated = True
                    logger.info(f"Deprecated method: {m.name} ({reason})")
                    return True
        return False

    def get_methods(self, task_name: str, include_deprecated: bool = False) -> List[Method]:
        methods = self.methods.get(task_name, [])
        if not include_deprecated:
            methods = [m for m in methods if not m.deprecated]
        return sorted(methods, key=lambda m: -m.effective_priority)

    def get_task(self, name: str) -> Optional[TaskDefinition]:
        return self.tasks.get(name)

    def is_primitive(self, name: str) -> bool:
        task = self.tasks.get(name)
        return task.is_primitive if task else False

    def find_tasks_by_tag(self, tag: str) -> List[TaskDefinition]:
        return [t for t in self.tasks.values() if tag in t.tags and not t.deprecated]

    def record_trace(self, trace: PlanTrace):
        self.plan_traces.append(trace)
        if len(self.plan_traces) > 1000:
            self.plan_traces = self.plan_traces[-1000:]

    def _sort_methods(self, task_name: str):
        if task_name in self.methods:
            self.methods[task_name].sort(key=lambda m: -m.effective_priority)

    def _method_signature(self, method: Method) -> str:
        content = json.dumps({"subtasks": method.subtasks,
                              "preconditions": sorted(method.preconditions.keys())},
                             sort_keys=True)
        return hashlib.md5(content.encode()).hexdigest()

    def get_statistics(self) -> Dict[str, Any]:
        task_counts = defaultdict(int)
        for t in self.tasks.values():
            task_counts[t.mutability.value] += 1
        method_counts = defaultdict(int)
        total_methods = 0
        for methods in self.methods.values():
            for m in methods:
                if not m.deprecated:
                    method_counts[m.origin.value] += 1
                    total_methods += 1
        return {
            "total_tasks": len(self.tasks),
            "tasks_by_mutability": dict(task_counts),
            "total_methods": total_methods,
            "methods_by_origin": dict(method_counts),
            "total_traces": len(self.plan_traces),
        }


