@dataclass
class TaskDefinition:
    """
    A reusable definition of a task — either primitive or compound.
    This is the TEMPLATE, not a running instance.
    """
    name: str
    description: str = ""
    is_primitive: bool = False
    mutability: TaskMutability = TaskMutability.LEARNED
    preconditions: Dict[str, Any] = field(default_factory=dict)
    effects: Dict[str, Any] = field(default_factory=dict)
    cost: float = 1.0
    tags: List[str] = field(default_factory=list)
    created_at: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())
    created_by: str = "system"
    usage_count: int = 0
    success_count: int = 0
    failure_count: int = 0
    deprecated: bool = False

    @property
    def success_rate(self) -> float:
        if self.usage_count == 0:
            return 0.5  # Neutral prior
        return self.success_count / self.usage_count

    @property
    def is_modifiable(self) -> bool:
        return self.mutability != TaskMutability.ANCHORED

    def to_dict(self) -> dict:
        return {
            "name": self.name,
            "description": self.description,
            "is_primitive": self.is_primitive,
            "mutability": self.mutability.value,
            "preconditions": {k: str(v) if callable(v) else v
                              for k, v in self.preconditions.items()},
            "effects": {k: str(v) if callable(v) else v
                        for k, v in self.effects.items()},
            "cost": self.cost,
            "tags": self.tags,
            "created_at": self.created_at,
            "created_by": self.created_by,
            "usage_count": self.usage_count,
            "success_rate": self.success_rate,
            "deprecated": self.deprecated,
        }


@dataclass
class Method:
    """
    A decomposition rule: one way to accomplish a compound task.
    Methods are living objects — they track their own performance
    and can be created, modified, or deprecated at runtime.
    """
    method_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    name: str = ""
    task_name: str = ""
    description: str = ""
    preconditions: Dict[str, Any] = field(default_factory=dict)
    subtasks: List[str] = field(default_factory=list)
    subtask_params: List[Dict[str, Any]] = field(default_factory=list)
    priority: int = 0
    origin: MethodOrigin = MethodOrigin.BUILT_IN
    confidence: float = 0.5     # How confident we are this method works
    usage_count: int = 0
    success_count: int = 0
    failure_count: int = 0
    avg_execution_time: float = 0.0
    created_at: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())
    created_by: str = "system"
    deprecated: bool = False
    autoresearch_generation: int = 0  # Which AR iteration produced this

    def is_applicable(self, state: WorldState) -> bool:
        return state.satisfies(self.preconditions)

    @property
    def success_rate(self) -> float:
        if self.usage_count == 0:
            return self.confidence  # Use prior confidence
        return self.success_count / self.usage_count

    @property
    def effective_priority(self) -> float:
        """Priority weighted by track record. Better methods float up."""
        base = self.priority
        track_record = self.success_rate
        recency_bonus = 0.1 if self.origin == MethodOrigin.AUTORESEARCH else 0
        return base + (track_record * 2) + recency_bonus

    def record_outcome(self, success: bool, execution_time: float = 0):
        self.usage_count += 1
        if success:
            self.success_count += 1
        else:
            self.failure_count += 1
        if self.avg_execution_time == 0:
            self.avg_execution_time = execution_time
        else:
            self.avg_execution_time = (
                0.8 * self.avg_execution_time + 0.2 * execution_time
            )
        self.confidence = 0.3 * self.confidence + 0.7 * self.success_rate

    def to_dict(self) -> dict:
        return {
            "method_id": self.method_id,
            "name": self.name,
            "task_name": self.task_name,
            "description": self.description,
            "preconditions": {k: str(v) if callable(v) else v
                              for k, v in self.preconditions.items()},
            "subtasks": self.subtasks,
            "priority": self.priority,
            "effective_priority": self.effective_priority,
            "origin": self.origin.value,
            "confidence": self.confidence,
            "success_rate": self.success_rate,
            "usage_count": self.usage_count,
            "deprecated": self.deprecated,
            "created_by": self.created_by,
        }


@dataclass
class PrimitiveTask:
    """A concrete task instance ready for execution."""
    name: str
    params: Dict[str, Any] = field(default_factory=dict)
    preconditions: Dict[str, Any] = field(default_factory=dict)
    effects: Dict[str, Any] = field(default_factory=dict)
    cost: float = 1.0
    task_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    status: TaskStatus = TaskStatus.PENDING
    result: Optional[Any] = None

    def is_applicable(self, state: WorldState) -> bool:
        return state.satisfies(self.preconditions)

    def apply(self, state: WorldState):
        state.apply_effects(self.effects)


@dataclass
class PlanTrace:
    """Records a complete planning + execution episode for learning."""
    trace_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    root_task: str = ""
    method_chain: List[str] = field(default_factory=list)
    primitive_sequence: List[str] = field(default_factory=list)
    initial_state: Dict[str, Any] = field(default_factory=dict)
    final_state: Dict[str, Any] = field(default_factory=dict)
    success: bool = False
    total_cost: float = 0.0
    total_time: float = 0.0
    timestamp: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())
    context: Dict[str, Any] = field(default_factory=dict)


