class ExperienceLearner:
    """
    Watches plan execution. Promotes recurring successes into methods.
    Deprecates consistently failing methods.
    """

    def __init__(self, registry: DynamicTaskRegistry):
        self.registry = registry
        self.promotion_threshold = 3
        self.deprecation_threshold = 0.2
        self.pattern_counts: Dict[str, Dict[str, int]] = defaultdict(lambda: defaultdict(int))

    def observe_outcome(self, trace: PlanTrace):
        self.registry.record_trace(trace)
        pattern_key = "|".join(trace.primitive_sequence)
        if trace.success:
            self.pattern_counts[trace.root_task]["success:" + pattern_key] += 1
            self._check_promotion(trace)
        else:
            self.pattern_counts[trace.root_task]["failure:" + pattern_key] += 1
        for method_name in trace.method_chain:
            for methods_list in self.registry.methods.values():
                for m in methods_list:
                    if m.name == method_name:
                        m.record_outcome(trace.success, trace.total_time)
        self._check_deprecations()

    def _check_promotion(self, trace: PlanTrace):
        pattern_key = "|".join(trace.primitive_sequence)
        count = self.pattern_counts[trace.root_task]["success:" + pattern_key]
        if count >= self.promotion_threshold:
            existing = self.registry.get_methods(trace.root_task)
            for m in existing:
                if m.subtasks == trace.primitive_sequence:
                    return
            new_method = Method(
                name=f"{trace.root_task}_learned_{len(existing)}",
                task_name=trace.root_task,
                description=f"Learned from {count} successful executions",
                subtasks=trace.primitive_sequence,
                priority=1, origin=MethodOrigin.EXPERIENCE, confidence=0.7,
                created_by="experience_learner",
            )
            self.registry.add_method(new_method)

    def _check_deprecations(self):
        for task_name, methods in self.registry.methods.items():
            for m in methods:
                if (m.usage_count >= 10 and
                    m.success_rate < self.deprecation_threshold and
                    m.mutability != TaskMutability.ANCHORED and
                    not m.deprecated):
                    self.registry.deprecate_method(
                        m.method_id,
                        f"Success rate {m.success_rate:.0%} below threshold"
                    )

    def suggest_new_task(self, goal_description: str,
                          tags: List[str] = None) -> TaskDefinition:
        name = goal_description.lower().replace(" ", "_")[:50]
        if name in self.registry.tasks:
            name = f"{name}_{uuid.uuid4().hex[:6]}"
        task = TaskDefinition(
            name=name, description=goal_description,
            is_primitive=False, mutability=TaskMutability.LEARNED,
            tags=tags or ["self_directed", "novel"],
            created_by="sophia",
        )
        self.registry.add_task(task)
        return task


