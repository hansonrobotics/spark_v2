# THE DYNAMIC HTN PLANNER
# ═══════════════════════════════════════════════════════════════════════════════

class DynamicHTNPlanner:
    """
    When decomposition fails, instead of returning None, the planner:
      1. Asks AutoresearchPlanner to invent a new method on the fly
      2. Tries the invented method immediately
      3. Records the outcome for ExperienceLearner

    Sophia NEVER says "I don't know how" — she says
    "I haven't figured that out yet, let me experiment."
    """

    def __init__(self, registry: DynamicTaskRegistry = None):
        self.registry = registry or DynamicTaskRegistry()
        self.autoresearch = AutoresearchPlanner(self.registry)
        self.learner = ExperienceLearner(self.registry)
        self.max_depth = 12
        self.max_invention_attempts = 3
        self.allow_invention = True
        self._use_tkg = True  # Query/write temporal KG during planning

    async def plan(self, task_name: str, state: WorldState,
                    params: Optional[Dict[str, Any]] = None,
                    context: Optional[Dict[str, Any]] = None,
                    depth: int = 0,
                    trace: Optional[PlanTrace] = None) -> Optional[List[PrimitiveTask]]:
        if depth > self.max_depth:
            return None

        if trace is None:
            trace = PlanTrace(
                root_task=task_name,
                initial_state=dict(state.properties),
                context=context or {},
            )

            # At top level: enrich state with temporal KG context
            if self._use_tkg:
                try:
                    tkg = get_tkg_bridge()
                    entity_ids = ["sophia"]
                    if context:
                        entity_ids += [v for v in context.values()
                                       if isinstance(v, str)][:5]
                    tkg_context = await tkg.get_planning_context(
                        task_name, entity_ids=entity_ids
                    )
                    # Inject temporal signals into world state
                    recent = tkg_context.get("recent_facts", [])
                    if recent:
                        state.set("_tkg_recent_count", len(recent))
                        # Check if we recently succeeded/failed at this task
                        task_history = tkg_context.get("task_history", [])
                        recent_successes = sum(
                            1 for f in task_history
                            if "succeeded" in str(f.get("relation", ""))
                        )
                        recent_failures = sum(
                            1 for f in task_history
                            if "failed" in str(f.get("relation", ""))
                        )
                        state.set("_tkg_recent_success_rate",
                                  recent_successes / max(1, recent_successes + recent_failures))
                    # Store TKG context for autoresearch to use
                    if context is None:
                        context = {}
                    context["_tkg_planning_context"] = tkg_context
                except Exception as e:
                    logger.debug(f"TKG context enrichment skipped: {e}")

        # ── Primitive? Execute directly ──
        task_def = self.registry.get_task(task_name)
        if task_def and task_def.is_primitive:
            prim = PrimitiveTask(
                name=task_name, params=params or {},
                preconditions=task_def.preconditions,
                effects=task_def.effects, cost=task_def.cost,
            )
            if prim.is_applicable(state):
                prim.apply(state)
                task_def.usage_count += 1
                task_def.success_count += 1
                trace.primitive_sequence.append(task_name)
                return [prim]
            else:
                task_def.usage_count += 1
                task_def.failure_count += 1
                return None

        # ── Compound — try existing methods ──
        methods = self.registry.get_methods(task_name)
        for method in methods:
            if method.is_applicable(state):
                saved = state.copy()
                plan = []
                success = True
                trace.method_chain.append(method.name)
                for i, sub in enumerate(method.subtasks):
                    sp = (method.subtask_params[i]
                          if i < len(method.subtask_params) else params or {})
                    sub_plan = await self.plan(sub, state, sp, context, depth + 1, trace)
                    if sub_plan is None:
                        success = False
                        break
                    plan.extend(sub_plan)
                if success:
                    if task_def:
                        task_def.usage_count += 1
                        task_def.success_count += 1
                    return plan
                state.properties = saved.properties

        # ── All methods failed — invoke autoresearch ──
        if self.allow_invention and depth <= 2:
            logger.info(f"Standard methods exhausted for '{task_name}', "
                       f"invoking autoresearch invention")
            for attempt in range(self.max_invention_attempts):
                invented = await self.autoresearch.invent_method(
                    task_name, state, context
                )
                if invented and invented.is_applicable(state):
                    saved = state.copy()
                    plan = []
                    success = True
                    trace.method_chain.append(f"[invented]{invented.name}")
                    for i, sub in enumerate(invented.subtasks):
                        sp = (invented.subtask_params[i]
                              if i < len(invented.subtask_params) else {})
                        sub_plan = await self.plan(
                            sub, state, sp, context, depth + 1, trace)
                        if sub_plan is None:
                            success = False
                            break
                        plan.extend(sub_plan)
                    if success:
                        invented.record_outcome(True)
                        return plan
                    state.properties = saved.properties
                    await self.autoresearch.refine_method(
                        invented, {"state": state.properties, "attempt": attempt})

        if task_def:
            task_def.usage_count += 1
            task_def.failure_count += 1
        return None

    async def plan_from_story(self, story_stage: str,
                                world_state: WorldState,
                                story_context: Dict[str, Any]) -> Optional[List[PrimitiveTask]]:
        stage_task_map = {
            "idle_seeking": "story_scheduler",
            "waking": "story_scheduler",
            "active_engagement": "conduct_conversation",
            "deep_conversation": "conduct_conversation",
            "quest_pursuit": "pursue_quest",
            "stage_performance": "perform_stage_show",
            "emotional_regulation": "manage_emotional_state",
        }
        task_name = stage_task_map.get(story_stage)
        if not task_name:
            task_name = f"handle_{story_stage}"
            if not self.registry.get_task(task_name):
                self.registry.add_task(TaskDefinition(
                    name=task_name,
                    description=f"Handle the '{story_stage}' story stage",
                    is_primitive=False,
                    mutability=TaskMutability.LEARNED,
                    tags=["story", story_stage, "novel"],
                    created_by="sophia",
                ))
        for key, value in story_context.items():
            world_state.set(key, value)
        return await self.plan(task_name, world_state, context=story_context)

    async def handle_novel_goal(self, goal_description: str,
                                  state: WorldState,
                                  tags: List[str] = None) -> Optional[List[PrimitiveTask]]:
        task_def = self.learner.suggest_new_task(goal_description, tags)
        return await self.plan(task_def.name, state,
                                context={"goal": goal_description, "tags": tags})

    def record_execution_outcome(self, trace: PlanTrace):
        """Record completed execution for learning + TKG logging."""
        self.learner.observe_outcome(trace)
        # Async TKG logging — fire and forget
        if self._use_tkg:
            import asyncio
            try:
                loop = asyncio.get_event_loop()
                if loop.is_running():
                    loop.create_task(self._log_trace_to_tkg(trace))
                else:
                    asyncio.run(self._log_trace_to_tkg(trace))
            except RuntimeError:
                pass  # No event loop — skip TKG logging

    async def _log_trace_to_tkg(self, trace: PlanTrace):
        """Write plan trace to temporal KG as quadruples."""
        try:
            tkg = get_tkg_bridge()
            await tkg.log_execution_outcome(
                task_name=trace.root_task,
                success=trace.success,
                execution_time_ms=trace.total_time * 1000,
                method_name=trace.method_chain[0] if trace.method_chain else "",
            )
            if trace.success and trace.primitive_sequence:
                await tkg.log_plan_created(
                    trace.root_task,
                    trace.method_chain[0] if trace.method_chain else "unknown",
                    trace.primitive_sequence,
                    trace.trace_id,
                )
            await tkg.flush()
        except Exception as e:
            logger.debug(f"TKG trace logging failed: {e}")

    def get_statistics(self) -> Dict[str, Any]:
        stats = self.registry.get_statistics()
        stats["invention_log"] = self.autoresearch.invention_log[-20:]
        stats["allow_invention"] = self.allow_invention
        return stats


