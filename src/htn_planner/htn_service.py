class TaskStatus(str, Enum):
    PENDING = "pending"
    ACTIVE = "active"
    COMPLETED = "completed"
    FAILED = "failed"
    SUSPENDED = "suspended"


class TaskMutability(str, Enum):
    """How changeable a task definition is."""
    ANCHORED = "anchored"      # Hardware-bound, immutable (motor commands)
    STABLE = "stable"          # Core behaviors, rarely changed
    LEARNED = "learned"        # Discovered through experience
    EXPERIMENTAL = "experimental"  # Under autoresearch evaluation
    EPHEMERAL = "ephemeral"    # One-shot, not yet promoted


class MethodOrigin(str, Enum):
    """How a method came into existence."""
    BUILT_IN = "built_in"          # Shipped with the system
    LLM_INVENTED = "llm_invented"  # Generated by LLM on demand
    EXPERIENCE = "experience"      # Promoted from a successful ad-hoc plan
    AUTORESEARCH = "autoresearch"  # Discovered via autoresearch loop
    COMPOSED = "composed"          # Automatically composed from subpatterns


@dataclass
class WorldState:
    """The HTN working world state, modified during planning."""
    properties: Dict[str, Any] = field(default_factory=dict)

    def get(self, key: str, default=None):
        return self.properties.get(key, default)

    def set(self, key: str, value: Any):
        self.properties[key] = value

    def satisfies(self, preconditions: Dict[str, Any]) -> bool:
        for key, expected in preconditions.items():
            actual = self.properties.get(key)
            if callable(expected):
                if not expected(actual):
                    return False
            elif actual != expected:
                return False
        return True

    def apply_effects(self, effects: Dict[str, Any]):
        for key, value in effects.items():
            if callable(value):
                self.properties[key] = value(self.properties.get(key))
            else:
                self.properties[key] = value

    def copy(self) -> "WorldState":
        return WorldState(properties=dict(self.properties))

    def diff(self, other: "WorldState") -> Dict[str, Any]:
        """What changed between two states."""
        changes = {}
        all_keys = set(self.properties) | set(other.properties)
        for k in all_keys:
            v1 = self.properties.get(k)
            v2 = other.properties.get(k)
            if v1 != v2:
                changes[k] = {"from": v1, "to": v2}
        return changes


