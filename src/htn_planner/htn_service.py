# AUTORESEARCH AS A PLANNING STRATEGY
# ═══════════════════════════════════════════════════════════════════════════════

class AutoresearchPlanner:
    """
    When standard HTN decomposition fails, this module treats the
    problem as an autoresearch challenge:
      1. Formulate the task as an optimization target
      2. Use an LLM agent to propose candidate methods
      3. Evaluate each candidate via simulation
      4. Keep the best, discard the rest
      5. Promote successful methods into the registry

    Autoresearch is thus a FIRST-CLASS planning strategy.
    "I don't know how to do X yet, so I'll experiment."
    """

    def __init__(self, registry: DynamicTaskRegistry):
        self.registry = registry
        self.invention_log: List[Dict[str, Any]] = []
        self.max_candidates = 5
        self.min_confidence_to_promote = 0.6

    async def invent_method(self, task_name: str, state: WorldState,
                              context: Dict[str, Any] = None) -> Optional[Method]:
        task_def = self.registry.get_task(task_name)
        if not task_def:
            return None
        logger.info(f"AutoresearchPlanner: inventing method for '{task_name}'")

        # Query temporal KG for context that informs invention
        tkg = get_tkg_bridge()
        temporal_context = await tkg.get_planning_context(
            task_name,
            entity_ids=list(set(["sophia"] + list((context or {}).keys())[:5])),
        )

        program = self._build_invention_prompt(task_def, state, context)
        candidates = await self._generate_candidates(
            task_def, state, program, temporal_context
        )
        if not candidates:
            return None
        best_candidate = None
        best_score = -1
        for candidate in candidates:
            score = await self._evaluate_candidate(candidate, state, temporal_context)
            if score > best_score:
                best_score = score
                best_candidate = candidate
        if best_candidate and best_score >= self.min_confidence_to_promote:
            best_candidate.confidence = best_score
            best_candidate.origin = MethodOrigin.AUTORESEARCH
            best_candidate.created_by = "autoresearch_planner"
            self.registry.add_method(best_candidate)
            # Log invention to TKG
            await tkg.log_method_invented(
                best_candidate.name, task_name, best_score
            )
            self.invention_log.append({
                "task": task_name,
                "method": best_candidate.name,
                "confidence": best_score,
                "candidates_evaluated": len(candidates),
                "timestamp": datetime.now(timezone.utc).isoformat(),
                "temporal_facts_used": len(temporal_context.get("recent_facts", [])),
            })
            return best_candidate
        return None

    async def refine_method(self, method: Method,
                              failure_context: Dict[str, Any]) -> Optional[Method]:
        variants = await self._generate_variants(method, failure_context)
        best = None
        best_score = method.confidence
        for variant in variants:
            score = await self._evaluate_candidate(
                variant, WorldState(properties=failure_context.get("state", {}))
            )
            if score > best_score:
                best_score = score
                best = variant
        if best:
            best.origin = MethodOrigin.AUTORESEARCH
            best.confidence = best_score
            best.autoresearch_generation = method.autoresearch_generation + 1
            best.created_by = "autoresearch_refinement"
            self.registry.add_method(best)
            return best
        return None

    def _build_invention_prompt(self, task_def: TaskDefinition,
                                  state: WorldState,
                                  context: Dict[str, Any] = None) -> str:
        available_primitives = [
            t.name for t in self.registry.tasks.values()
            if t.is_primitive and not t.deprecated
        ]
        available_compounds = [
            t.name for t in self.registry.tasks.values()
            if not t.is_primitive and not t.deprecated and t.name != task_def.name
        ]
        return f"""# Autoresearch: Invent Method for '{task_def.name}'
## Task: {task_def.description}
## Tags: {', '.join(task_def.tags)}
## Current State: {json.dumps(state.properties, indent=2, default=str)}
## Available Primitives: {', '.join(sorted(available_primitives))}
## Available Compounds: {', '.join(sorted(available_compounds))}
## Context: {json.dumps(context or {}, indent=2, default=str)}
"""

    async def _generate_candidates(self, task_def: TaskDefinition,
                                     state: WorldState,
                                     program: str,
                                     temporal_context: Dict = None) -> List[Method]:
        candidates = []
        available = [t.name for t in self.registry.tasks.values()
                     if t.is_primitive and not t.deprecated]

        # Strategy 0: LLM-invented method (primary strategy)
        llm = get_llm_client()
        available_compounds = [
            t.name for t in self.registry.tasks.values()
            if not t.is_primitive and not t.deprecated
            and t.name != task_def.name
        ]
        existing = [m.to_dict() for m in self.registry.get_methods(task_def.name)]
        temporal_facts = (temporal_context or {}).get("recent_facts", [])

        llm_result = await llm.invent_htn_method(
            task_name=task_def.name,
            task_description=task_def.description,
            available_primitives=available,
            available_compounds=available_compounds,
            world_state=state.properties,
            temporal_context=temporal_facts,
            existing_methods=existing,
        )
        if llm_result and isinstance(llm_result, dict):
            subtasks = llm_result.get("subtasks", [])
            # Validate all subtasks exist in registry
            valid = [s for s in subtasks if self.registry.get_task(s)]
            if valid:
                candidates.append(Method(
                    name=llm_result.get("name", f"{task_def.name}_llm_invented"),
                    task_name=task_def.name,
                    description=llm_result.get("description", "LLM-invented method"),
                    subtasks=valid,
                    preconditions=llm_result.get("preconditions", {}),
                    priority=2,
                    origin=MethodOrigin.LLM_INVENTED,
                    confidence=llm_result.get("confidence", 0.6),
                    created_by="sonnet_4",
                ))

        # Strategy 1: Tag-based composition (fallback)
        relevant = self.registry.find_tasks_by_tag(
            task_def.tags[0] if task_def.tags else "cognition"
        )
        if relevant:
            subtasks = [t.name for t in relevant[:5] if t.is_primitive]
            if subtasks:
                candidates.append(Method(
                    name=f"{task_def.name}_tag_composed",
                    task_name=task_def.name,
                    subtasks=subtasks, priority=0,
                    origin=MethodOrigin.COMPOSED, confidence=0.4,
                    created_by="autoresearch_tag_composer",
                ))

        # Strategy 2: Trace-based learning
        similar = self._find_similar_traces(task_def, state)
        for trace in similar[:2]:
            candidates.append(Method(
                name=f"{task_def.name}_from_trace_{trace.trace_id[:8]}",
                task_name=task_def.name,
                subtasks=trace.primitive_sequence[:8],
                priority=1, origin=MethodOrigin.EXPERIENCE, confidence=0.6,
                created_by="autoresearch_trace_learner",
            ))

        # Strategy 3: Generic cognitive pattern (last resort)
        generic = [s for s in ["recall", "reflect", "formulate_response", "speak"]
                   if s in available]
        if generic:
            candidates.append(Method(
                name=f"{task_def.name}_generic",
                task_name=task_def.name, subtasks=generic,
                priority=-1, origin=MethodOrigin.COMPOSED, confidence=0.3,
                created_by="autoresearch_generic",
            ))
        return candidates

    async def _generate_variants(self, method: Method, context: Dict) -> List[Method]:
        variants = []
        if "recall" not in method.subtasks:
            variants.append(Method(
                name=f"{method.name}_with_recall",
                task_name=method.task_name,
                subtasks=["recall"] + method.subtasks,
                preconditions=method.preconditions,
                priority=method.priority,
                created_by="autoresearch_variant_generator",
            ))
        variants.append(Method(
            name=f"{method.name}_reflective",
            task_name=method.task_name,
            subtasks=["reflect"] + method.subtasks + ["reflect", "update_knowledge"],
            preconditions=method.preconditions,
            priority=method.priority,
            created_by="autoresearch_variant_generator",
        ))
        return variants

    async def _evaluate_candidate(self, candidate: Method,
                                     state: WorldState,
                                     temporal_context: Dict = None) -> float:
        score = 0.3
        valid = sum(1 for s in candidate.subtasks
                    if self.registry.get_task(s) is not None)
        if candidate.subtasks:
            score += 0.3 * (valid / len(candidate.subtasks))
        if candidate.origin == MethodOrigin.EXPERIENCE:
            score += 0.2
        if candidate.origin == MethodOrigin.LLM_INVENTED:
            score += 0.15  # LLM-invented methods get a boost
        length_pen = max(0, 1.0 - len(candidate.subtasks) / 10)
        score += 0.1 * length_pen
        if len(set(candidate.subtasks)) < len(candidate.subtasks) * 0.5:
            score -= 0.2

        # Use LLM judge for higher-confidence evaluation if we have context
        if temporal_context and candidate.origin == MethodOrigin.LLM_INVENTED:
            try:
                llm = get_llm_client()
                task_def = self.registry.get_task(candidate.task_name)
                desc = task_def.description if task_def else candidate.task_name
                llm_score = await llm.evaluate_method_quality(
                    candidate.to_dict(), desc,
                    temporal_context.get("recent_facts", [])[:5],
                )
                # Blend heuristic and LLM scores
                score = 0.4 * score + 0.6 * llm_score
            except Exception:
                pass  # Fall back to heuristic score

        return max(0.0, min(1.0, score))

    def _find_similar_traces(self, task_def: TaskDefinition,
                               state: WorldState) -> List[PlanTrace]:
        return [t for t in self.registry.plan_traces
                if t.success and
                len(set(task_def.tags) & set(t.context.get("tags", []))) > 0]


